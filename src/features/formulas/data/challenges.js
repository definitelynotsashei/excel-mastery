import { FORMULAS_TRACK_ID } from "./constants";

function lockedCell(value) {
  return { value, editable: false };
}

function challenge({
  id,
  tier,
  title,
  department,
  scenario,
  learningObjective,
  supportedFunctions,
  targetCell,
  expectedFormula,
  expectedResult,
  hints,
  review,
  cells,
  xp,
  validationMode = "formula_result",
}) {
  return {
    id,
    track: FORMULAS_TRACK_ID,
    tier,
    title,
    department,
    accentColor: "violet",
    scenario,
    learningObjective,
    supportedFunctions,
    validationMode,
    grid: {
      columns: ["A", "B", "C", "D", "E", "F", "G", "H"],
      rows: 12,
      cells,
    },
    targetCells: [targetCell],
    expectedAnswer: {
      [targetCell]: {
        formulaPattern: expectedFormula,
        result: expectedResult,
      },
    },
    hints,
    review,
    xp,
  };
}

